		alt.sample.names <- gsub('-', '.', all.samples);
		}
	if ( (any(grepl("^[[:digit:]]", all.samples))) & (any(grepl('^X',rownames(callable.bases)))) ) {
		alt.sample.names <- paste0('X', alt.sample.names);
		}
