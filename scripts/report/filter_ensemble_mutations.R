sample.counts$NonSilent.Rate.Mb <- sample.counts$Non.Silent / sample.counts$Callable * 10**6;
sample.counts <- sample.counts[order(sample.counts$SNVsPerMb, decreasing = TRUE),];
sample.counts$Order <- 1:nrow(sample.counts);

